class Calculadora {

    // Soma de 2 inteiros
    public int somar(int a, int b) {
        return a + b;
    }

    // Soma de 3 inteiros
    public int somar(int a, int b, int c) {
        return a + b + c;
    }

    // Soma de 2 doubles
    public double somar(double a, double b) {
        return a + b;
    }
}